package session39;

public class bitmasking {

	public static void main(String[] args) {

		System.out.println(1 & 1);
		System.out.println(1 & 0);
		System.out.println(0 & 1);
		System.out.println(0 & 0);
		System.out.println(1 | 1);
		System.out.println(1 | 0);
		System.out.println(0 | 1);
		System.out.println(0 | 0);
		System.out.println(0^0);
		System.out.println(0^1);

	}

}
